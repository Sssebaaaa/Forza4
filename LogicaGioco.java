public class LogicaGioco {

    private char[][] tabellone = new char[6][7];

    public void inizializzaTabellone(){
        for(int i=0; i<tabellone.length; i++){
            for(int j=0; j<tabellone[i].length; j++){
                tabellone[i][j] = ' ';
            }
        }
    }
    

     


}